export function getSeedVerses() {
  // Multiply the seed verses to fill 365 days by looping over them
  const daysInYear = 365;
  const filledVerses = [];
  
  for (let i = 0; i < daysInYear; i++) {
    const seedVerse = seedVerses[i % seedVerses.length];
    filledVerses.push({
      ...seedVerse,
      dayOfYear: i + 1,
      id: crypto.randomUUID(),
      active: true,
      isFavorite: false,
      createdAt: new Date().toISOString(),
      updatedAt: new Date().toISOString(),
      deletedAt: null
    });
  }
  
  return filledVerses;
}
